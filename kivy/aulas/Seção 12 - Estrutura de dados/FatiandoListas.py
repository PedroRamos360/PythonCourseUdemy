lista = "Bem-vindo ao Curso de python"
# para fatiar listas nós usamos
# lista[start:stop:step]
# start: onde começamos
# stop: onde paramos
# step: pulo de item em item

print(lista[:20]) # Pega todas as letras até 20
print(lista[::2]) # Pega todas as letras pulando uma de cada vez
print(lista[::-1]) # Mostra a string invertida
print(lista[22:]) # Pega todas as letras a partir do 22